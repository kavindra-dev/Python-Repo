#operator used for comparision and return boolean value

print(3 > 2)            #greator than
print(3 < 2)            #less than
print(2 == 2)           #equals
print(3 != 2)           #not equals
print(3 <= 3)           #less than or equals, so on.
print(3 >= 3)           #greater than or equals, so on.


#logical operator
# they are : or , and ,  not

print(2 > 3 or 2 > 1)   # Or-operator : Any one condition true the return true
print(2 > 3 and 3 < 4)  # And-operator : Both condition need to be true then return true
print(not 2 > 3 )       # Not-opertor : reverse the return value, here 2 > 3 return false but using not operator will return true

