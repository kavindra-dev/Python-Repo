#In this section we will be learning about string and different supported function with it
name = "Data Analysit"      #Define a String of some value

print(name)

#Convert the name into Upper case without effecting the original
print(name.upper())


#Help in finding mentioned text ot character from the given text.
print(name.find('Data'))

# Used for replacing a character or word with new one
print(name.replace("Data", "Programmer"))

# Used for finding in the given text.
print("Data" in name)