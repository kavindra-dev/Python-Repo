# function is something that perform specific operartion whenever a call is made. Function in python are of 3 type
#1. In-Built Function           2. Module Function      3. User-defined Function

#1. In-Built Function   : 
#ex : int(), str(), bool(), etc.            #Already exist in python that we uses, see below.

st = 2
print(st)
print(int(st))              #integer function return integer value i.e; 2 as int
print(str(st))              #string function return string value i.e; 2 as String
print(float(st))            #float function return float value i.e; 2.0 as float
print(bool(st == 2))        #boolean value return true or false i.e; true as st is equal to true.
